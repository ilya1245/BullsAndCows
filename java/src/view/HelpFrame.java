package view;

import java.awt.Component;
import java.awt.Frame;
import javax.swing.JButton;
import javax.swing.JDialog;
import javax.swing.JPanel;
import javax.swing.JScrollPane;
import javax.swing.JTextArea;

class HelpFrame extends JDialog {
    private static final String RULES =
            "  Все очень просто.\n" +
            "  Компьютер задумывает число. Оно может быть от трехзначного до шестизначного" +
            " по Вашему усмотрению (Меню Опции). По умолчанию число четырехзначное." +
            " Цифры не повторяются и первая цифра не ноль. Ваша цель - разгадать это число.\n\n" +
            "  Как это сделать.\n" +
            "  В Меню Файл Вы начинаете игру. Делая ход Вы предлагаете свой вариант," +
            " а компьютер сообщает Вам горячо или холодно следующим образом: он пишет," +
            " сколько Быков и Коров в Вашем числе. Бык - это угаданная Вами цифра, стоящая" +
            " на своем месте, а Корова - это угаданная цифра стоящая не на своем месте.\n\n" +
            "  Например:\n" +
            "  Компьютер задумал число: 8203   Ваш вариант: 7235\n" +
            "Компьютер ответит: 1 Бык  1 Корова.\n" +
            "Вы угадали цифру \"2\" и ее место в числе. Это - Бык. Вы угадали цифру \"3\"," +
            " но не угадали ее место. Это - Корова. Следовательно, Вам нужны 4 Быка." +
            " Если правильно рассуждать, то 4-х значное число можно отгадать за 7-9 попыток максимум." +
            " Делая новый ход проверьте его на соответствие всем ответам компьютера, иначе ход напрасен.\n\n" +
            "  Если не смогли разгадать, то вместо Вашего варианта введите слово number," +
            " и компьютер чистосердечно признается, но это можно сделать не раньше 5-го хода.\n\n" +
            "  Моя девушка научилась неплохо играть за несколько дней, и ей даже понравилось," +
            " хотя по-началу были слезы. Но у девушек это бывает, особенно когда в игре надо" +
            " напрячь голову, а проигрывать не хочется.\n\n" +
            "  В эту игру могут играть два человека друг против друга. Оба задумывают и по очереди" +
            " ходят. Но советую потренироваться с компьютером, чтобы лёгко вздрючить своих знакомых.\n\n" +
            "  В данной версии игры компьютер может быть Вашим спаринг-партнером. Уровень его" +
            " интеллекта Вы можете выбирать в меню Опции >> Мастерство компьютера. Если спаринг-партнер" +
            " не требуется, то выберите соответствующий тип игры в меню Опции >> Тип игры.\n\n" +
            "  Автор выражает благодарность Третьяку Андрею и Шевченко Александру за их замечания и предложения.";

    HelpFrame(Frame owner) {
        super(owner, "Правила игры", false);

        JTextArea textArea = new JTextArea(RULES);
        textArea.setLineWrap(true);
        textArea.setWrapStyleWord(true);
        textArea.setEditable(false);

        JButton btnOk = new JButton("Ok");
        btnOk.addActionListener(e -> setVisible(false));

        JPanel keyPanel = new JPanel();
        keyPanel.add(btnOk);

        setSize(600, 570);
        setLocationRelativeTo(owner);

        getContentPane().add((Component) new JScrollPane(textArea), "Center");
        getContentPane().add((Component) keyPanel, "South");
    }
}
